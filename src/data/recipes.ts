/** 为配方数据注入运行时 id 字段 */
function injectId(raw: Record<string, Omit<Recipe, 'id'>>): Record<string, Recipe> {
  return Object.fromEntries(
    Object.entries(raw).map(([id, recipe]) => [id, { ...recipe, id }])
  ) as Record<string, Recipe>
}

